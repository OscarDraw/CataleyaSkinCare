                <h3>¿Por qué aparecen los puntos negros?</h3>
                <span class="date-label">28-11-2020 - Jhan Arciniegas</span>
                <ul>
                    <li>Los puntos negros son un exceso del sebo que produce la piel que, al contacto con el aire, se oxidan y adquieren un color oscuros.</li>
                    <li><b>Piel grasa:</b> las pieles mixtas a grasas generan más grasa en la piel por lo que son más susceptibles de tener puntos negros.</li>
                    <li><b>Falta de limpieza:</b> Si no se retira bien la suciedad y los restos de maquillaje de la piel, éstos pueden quedarse acumulados en los poros y obstruirlos. Así, provocan la aparición de puntos negros. Por eso, es recomendable que no te saltes nunca tu rutina de cuidado facial, sobre todo si has usado maquillaje.</li>
                    <li><b>Clima:</b>Estar expuesto a temperaturas extremas, puede provocar que los poros se dilaten y sean más susceptibles de acumular restos de suciedad. </li>
                    <li><b>Contaminación:</b>La exposición al aire contaminado, muy propio de las grandes ciudades, también puede favorecer la aparición de puntos negros en la cara. </li>
                </ul>
